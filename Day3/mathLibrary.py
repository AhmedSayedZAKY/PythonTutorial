import math

#math.ceil
a=2.354
print ("The ceil of 2.354 is : ", end="") 
print (math.ceil(a)) 

#math.floor
b=2.354
print ("The floor of 2.354 is : ", end="") 
print (math.floor(b)) 

#math.sqrt
c=25
print ("The sqrt of 25 is : ", end="") 
print (math.sqrt(c)) 

#math.fabs
d=-6
print ("The fabs of 6 is : ", end="") 
print (math.fabs(d)) 

#math.factorial
e=7
print ("The fact of 7 is : ", end="") 
print (math.factorial(e)) 

#math.pi
print (math.pi) 

#copysign   
# returning the copysigned value. 
print ("The copysigned value of -10 and 5.5 is : ", end="") 
print (math.copysign(5.5, -10)) 
  
# returning the gcd of 15 and 5 
# gcd is a mathematical expression to find the highest number 
# which can divide both the numbers whose gcd has to be found 
# with the resulting remainder as zero
print ("The gcd of 5 and 15 is : ", end="") 
print (math.gcd(5,15)) 





